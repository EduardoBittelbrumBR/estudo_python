# entrada de valores
n1 = int(input('Digite o primeiro número: '))
n2 = int(input('Digite o segundo número: '))

# calculos

s = n1 + n2
sub = n1 - n2
div = n1 / n2
mult = n1 * n2
di = n1 // n2
pot = n1 ** n2

print('Valor soma: {}'.format(s))
print('Valor subtração: {}'.format(sub))
print('Valor divisão: {}'.format(div))
print('Valor multiplicação: {}'.format(mult))
print('Valor divisão inteira: {}'.format(di))
print('Valor potência: {}'.format(pot))
